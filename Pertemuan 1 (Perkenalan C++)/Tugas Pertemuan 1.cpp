#include <iostream>
using namespace std;
int main(){
	int w, x, y, z;
	cout << "SOAL NOMOR 1" << endl;
	cout << "Jika x=3, y=10 dan z=5. Maka x*y-z adalah..." << endl;
	cout << "Jawab :" << endl;
	cout << "Nilai x ="; cin >> x;
	cout << "Nilai y ="; cin >> y;
	cout << "Nilai z ="; cin >> z;
	cout << "x * y - z" << endl;
	cout << "3 * 10 -  5 ="; cin >> w;
	cout << "Hasil :" << x*y-z << endl;
	cout << "Jadi hasil perhitungan dari x*y-z adalah" << x*-z << endl;
	
	cout << "SOAL NOMOR 2" << endl;
	cout << "Sebuah balok mempunyai panjang 10cm, lebar 8cm dan tinggi 5cm. Hitunglah volume balok tersebut!" << endl;
	cout << "Jawab :" << endl;
	cout << "Panjang ="; cin >> w;
	cout << "Lebar ="; cin >> x;
	cout << "Tinggi ="; cin >> y;
	cout << "V = P * L * T" << endl;
	cout << "V = 10 * 8 * 5" << endl;
	cout << "V ="; cin >> z;
	cout << "Hasil :" << w*x*y << endl;
	cout << "Jadi hasil perhitungan volume sebuah balok adalah" << w*x*y << endl;
	
	cout << "SOAL NOMOR 3" << endl;
	cout << "Jarak antara Jakarta-Bandung adalah 195km. Jika ditempuh menggunakan mobil dengan kecepatan 65km/jam. Berapakah waktu yang diperlukan untuk menempuh jarak tersebut?" << endl;
	cout << "Jawab :" << endl;
	cout << "Jarak ="; cin >> x;
	cout << "Kecepatan ="; cin >> y;
	cout << "Waktu = Jarak/Kecepatan" << endl;
	cout << "Waktu = 195/65" << endl;
	cout << "Waktu ="; cin >> z;
	cout << "Hasil :" << x/y << endl;
	cout << "Jadi hasil perhitungan waktu antara Jakarta-Bandung adalah" << x/y << endl;
	
	return 0; 
}
